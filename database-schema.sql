create or replace view public.lead_summary
with (security_invoker = true) as
select
  l.id,
  l.session_id,
  l.source,
  l.status,
  l.name,
  l.email,
  l.phone,
  l.service,
  l.budget,
  l.urgency,
  l.message,
  l.notes,
  l.created_at,
  l.updated_at,
  count(cm.id) as chat_message_count,
  max(cm.created_at) as last_chat_message_at
from public.leads l
left join public.chat_messages cm on cm.session_id = l.session_id
group by l.id;

create or replace view public.session_timeline
with (security_invoker = true) as
select
  session_id,
  created_at,
  'chat_message' as activity_type,
  sender as actor,
  message as detail,
  jsonb_build_object('page', page, 'language', language) as metadata
from public.chat_messages
union all
select
  session_id,
  created_at,
  'contact_request' as activity_type,
  'cliente' as actor,
  concat(name, ' solicito ', service, ' con presupuesto ', budget) as detail,
  metadata
from public.contact_requests
union all
select
  session_id,
  created_at,
  event_type as activity_type,
  'system' as actor,
  event_type as detail,
  payload as metadata
from public.interaction_events;
