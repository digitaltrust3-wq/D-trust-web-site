create or replace function public.set_updated_at()
returns trigger as $$
begin
  new.updated_at = now();
  return new;
end;
$$ language plpgsql;

create or replace function public.prevent_more_than_five_active_admins()
returns trigger as $$
begin
  if new.is_active = true and (
    select count(*)
    from public.admin_profiles
    where is_active = true
      and user_id <> coalesce(new.user_id, '00000000-0000-0000-0000-000000000000'::uuid)
  ) >= 5 then
    raise exception 'Only five active admin users are allowed';
  end if;

  return new;
end;
$$ language plpgsql;

drop trigger if exists limit_active_admins on public.admin_profiles;

create trigger limit_active_admins
before insert or update on public.admin_profiles
for each row
